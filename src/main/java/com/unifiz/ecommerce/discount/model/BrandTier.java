package com.unifiz.ecommerce.discount.model;

/**
 * Enumeration representing different brand tiers in the e-commerce platform.
 * Used for categorizing brands and applying tier-specific discount logic.
 */
public enum BrandTier {
    /**
     * Premium tier brands - typically luxury or high-end brands
     */
    PREMIUM,
    
    /**
     * Regular tier brands - standard mid-range brands
     */
    REGULAR,
    
    /**
     * Budget tier brands - value or economy brands
     */
    BUDGET
} 